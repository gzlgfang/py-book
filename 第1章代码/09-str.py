# 09-str.py#字符串数据赋值
str1 = 'scut.edu.cn_华南理工大学邮箱'  # 单引号
str2 = "scut.edu.cn_'华南理工大学邮箱'"  # 双引号内有单引号
str3 = '''scut.edu.cn
        华南理工大学
        化学与化工学院
        '''  # 三引号
str4 = "scut.edu.cn"  # 双引号
str5 = ""'scut.edu.cn'  # 左右引号不等，多余的引号不起作用
str6 = """scut.edu.cn"""  # 三个双引号
str7 = ""  # 空字符串
print("str1=", str1)
print("str2=", str2)
print("str3=", str3)
print("str4=", str4)
print("str5=", str5)
print("str6=", str6)
print("str7=", str7)
