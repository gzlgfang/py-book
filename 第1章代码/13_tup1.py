# 元祖创建及其函数和方法调用
tup1 = (1, 2, 3, 'a', 'b', 'c')  # 元祖元素数字和字符串均可以
tup2 = 's', 'c', 'u', 't'  # 也可以不加括号
tup3 = (6,)  # 元素为单个数字时，需要添加逗号
tup4 = ('gz',)  # 元素为单个字符串时，需要添加逗号
tup5 = tuple([1, 2, 3])  # 列表转换为元祖(1,2,3)
tup6 = tuple("scut")  # 字符串转换成元祖('s', 'c', 'u', 't')
tup7 = tuple({'g', 'z'})  # 集合转换为元祖('g','z')
# 字典转换为元祖，只转换key('gz', 'hz', 'xm')
tup8 = tuple({'gz': 10, 'hz': 6, 'xm': 5.5})
